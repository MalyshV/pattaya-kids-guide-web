/**
 * Показывать ли демо-контент ([Демо]-место/события/занятие, isDemo=true).
 *
 * Демо полезно локально, пока реальных данных мало: на нём виден контраст
 * фильтров, слоты обложек, безместное занятие. Публичному пользователю демо
 * видеть нельзя («сайт не настоящий»), поэтому по умолчанию оно скрыто, а
 * локально включается переменной окружения SHOW_DEMO=true (лежит в .env,
 * который не попадает в деплой).
 */
export function showDemo(): boolean {
  return process.env.SHOW_DEMO === "true";
}

/**
 * Готовый кусочек where для публичных выборок: пустой, когда демо разрешено,
 * иначе отсекает isDemo. Модели Place/Event/PlaceProgram имеют одинаковое поле.
 */
export function demoFilter(): { isDemo?: false } {
  return showDemo() ? {} : { isDemo: false };
}
